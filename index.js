function add(a,b)
{
  var sum= a+b;
  return sum;
}

function subtract(a,b)
{
  var subtract= a-b;
  return subtract;
}

function divide(a,b)
{
  var sum= a/b;
  return sum;
}

function multiply(a,b)
{
  var sum= a*b;
  return sum;
}


function inc(a)
{
  a=a+1;
  return a;
}

function dec(a)

{
  a=a-1;
  return a;
}


